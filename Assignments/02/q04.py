import numpy as np

arr1 = np.array([4, 5, 6])
arr2 = np.array([1, 2, 3])

print("Array 1 :", arr1)
print("Array 2 :", arr2)

print("\nAddition :", np.add(arr1, arr2))
print("Subtraction :", np.subtract(arr1, arr2))
print("Multiplication :", np.multiply(arr1, arr2))
print("Division :", np.divide(arr1, arr2))